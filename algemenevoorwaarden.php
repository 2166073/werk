        <p>1.1 Rijschool: DriveSmart, gevestigd te Amsterdam, ingeschreven bij de Kamer van Koophandel onder nummer 11102006.</p>
        <p>1.2 Leerling: de persoon die bij de rijschool lessen volgt of een lespakket heeft afgenomen.</p>
        <p>1.3 Overeenkomst: de afspraak tussen rijschool en leerling over het volgen van rijlessen en/of examens.</p>
    </section>

    <section>
        <h2>Artikel 2 – Aanmelding en lesafspraken</h2>
        <p>2.1 Aanmelden kan telefonisch, per e-mail of via de website.</p>
        <p>2.2 De leerling verklaart in goede gezondheid te verkeren en in het bezit te zijn van een geldig legitimatiebewijs.</p>
        <p>2.3 Afsmeldingen moeten minimaal 24 uur van tevoren worden doorgegeven, anders wordt de les volledig in rekening gebracht.</p>
    </section>

    <section>
        <h2>Artikel 3 – Betalingen</h2>
        <p>3.1 Betaling van losse lessen of pakketten dient vooraf of uiterlijk op de dag van de les te gebeuren.</p>
        <p>3.2 Bij lespakketten geldt betaling in één keer, tenzij anders overeengekomen.</p>
        <p>3.3 Indien de leerling niet op tijd betaalt, behoudt de rijschool zich het recht voor om lessen stop te zetten tot de betaling is voldaan.</p>
    </section>

    <section>
        <h2>Artikel 4 – Rijlessen</h2>
        <p>4.1 Lessen duren standaard 60 minuten, tenzij anders afgesproken.</p>
        <p>4.2 De rijinstructeur bepaalt of een leerling geschikt is om een examen aan te vragen.</p>
        <p>4.3 De leerling dient instructies van de rijinstructeur op te volgen.</p>
    </section>

    <section>
        <h2>Artikel 5 – Examenaanvraag</h2>
        <p>5.1 De rijschool vraagt het praktijkexamen aan wanneer de instructeur oordeelt dat de leerling voldoende niveau heeft.</p>
        <p>5.2 Bij annulering van het examen door de leerling gelden de voorwaarden van het CBR. Eventuele kosten worden doorberekend.</p>
    </section>

    <section>
        <h2>Artikel 6 – Aansprakelijkheid</h2>
        <p>6.1 De rijschool is niet aansprakelijk voor schade veroorzaakt door de leerling tijdens de les of het examen, tenzij er sprake is van opzet of grove nalatigheid van de instructeur.</p>
        <p>6.2 De leerling is verantwoordelijk voor het tijdig doorgeven van wijzigingen in zijn/haar gezondheidstoestand of adresgegevens.</p>
    </section>

    <section>
        <h2>Artikel 7 – Beëindiging van de overeenkomst</h2>
        <p>7.1 De leerling kan de overeenkomst op elk moment schriftelijk beëindigen. Eventuele openstaande kosten worden verrekend.</p>
        <p>7.2 De rijschool behoudt zich het recht voor om bij ernstig wangedrag of wanbetaling de samenwerking per direct te beëindigen.</p>
    </section>

    <section>
        <h2>Artikel 8 – Klachten</h2>
        <p>8.1 Klachten over de rijschool of instructeur dienen binnen 7 dagen na de gebeurtenis schriftelijk te worden gemeld.</p>
        <p>8.2 De rijschool zal de klacht binnen een redelijke termijn behandelen.</p>
    </section>

